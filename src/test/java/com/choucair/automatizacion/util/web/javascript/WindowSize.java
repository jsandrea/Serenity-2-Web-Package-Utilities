public class WindowSize {
	
	private Long width;
	private Long height;
	
	public WindowSize(Long width, Long height) {
		this.width = width;
		this.height = height;
	}
	
	public Long getWidth() {
		return this.width;
	}
	
	public Long getHeight() {
		return this.height;
	}
	
}